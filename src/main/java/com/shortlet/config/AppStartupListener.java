    private void updateProperty(Connection connection, String oldTitle, String newTitle, String city, String address, String nightlyRate, String sourceUrl) throws SQLException {
        try (PreparedStatement statement = connection.prepareStatement(
                "UPDATE properties SET title = ?, city = ?, address = ?, nightly_rate = ?, source_url = ? WHERE title = ?")) {
            statement.setString(1, newTitle);
            statement.setString(2, city);
            statement.setString(3, address);
            statement.setBigDecimal(4, new java.math.BigDecimal(nightlyRate));
            statement.setString(5, sourceUrl);
            statement.setString(6, oldTitle);
            statement.executeUpdate();
        }
    }

    private void addColumnIfMissing(Statement statement, String table, String column, String definition) throws SQLException {
        statement.execute("ALTER TABLE " + table + " ADD COLUMN IF NOT EXISTS " + column + " " + definition);
    }

    private boolean hasRows(Connection connection, String tableName) throws SQLException {
        try (PreparedStatement statement = connection.prepareStatement("SELECT COUNT(*) FROM " + tableName);
             ResultSet rs = statement.executeQuery()) {
            rs.next();
            return rs.getInt(1) > 0;
        }
    }

    private void insertUser(Connection connection, String name, String email, String password, String role) throws SQLException {
        try (PreparedStatement statement = connection.prepareStatement(
                "INSERT INTO users(name, email, password_hash, role, auth_provider) VALUES (?, ?, ?, ?, 'LOCAL')")) {
            statement.setString(1, name);
            statement.setString(2, email);
            statement.setString(3, PasswordUtil.hash(password));
            statement.setString(4, role);
            statement.executeUpdate();
        }
    }

    private void insertPropertyIfMissing(Connection connection, String title, String city, String address, String nightlyRate, String imageUrl, String sourceUrl,
                                         double latitude, double longitude, boolean wifi, boolean selfCheckIn, boolean nearAirport, int replyTimeMinutes, double rating, String... secondaryImages) throws SQLException {
        long propertyId = 0;
        try (PreparedStatement check = connection.prepareStatement("SELECT id FROM properties WHERE title = ?")) {
            check.setString(1, title);
            try (ResultSet rs = check.executeQuery()) {
                if (rs.next()) {
                    propertyId = rs.getLong("id");
                }
            }
        }
        if (propertyId == 0) {
            try (PreparedStatement statement = connection.prepareStatement(
                    """
                    INSERT INTO properties(title, city, address, nightly_rate, image_url, source_url, latitude, longitude, wifi, self_check_in, near_airport, reply_time_minutes, rating)
                    VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)
                    """, Statement.RETURN_GENERATED_KEYS)) {
                statement.setString(1, title);
                statement.setString(2, city);
                statement.setString(3, address);
                statement.setBigDecimal(4, new java.math.BigDecimal(nightlyRate));
                statement.setString(5, imageUrl);
                statement.setString(6, sourceUrl);
                statement.setDouble(7, latitude);
                statement.setDouble(8, longitude);
                statement.setBoolean(9, wifi);
                statement.setBoolean(10, selfCheckIn);
                statement.setBoolean(11, nearAirport);
                statement.setInt(12, replyTimeMinutes);
                statement.setDouble(13, rating);
                statement.executeUpdate();
                try (ResultSet keys = statement.getGeneratedKeys()) {
                    if (keys.next()) {
                        propertyId = keys.getLong(1);
                    }
                }
            }
        }
        
        // Seed secondary images for this property
        if (propertyId > 0) {
            // First, delete any existing images to make seeding idempotent and clean
            try (PreparedStatement deleteImg = connection.prepareStatement("DELETE FROM property_images WHERE property_id = ?")) {
                deleteImg.setLong(1, propertyId);
                deleteImg.executeUpdate();
            }
            
            // Insert primary image
            try (PreparedStatement insertImg = connection.prepareStatement("INSERT INTO property_images(property_id, image_url) VALUES (?, ?)")) {
                insertImg.setLong(1, propertyId);
                insertImg.setString(2, imageUrl);
                insertImg.executeUpdate();
                
                for (String secImg : secondaryImages) {
                    insertImg.setLong(1, propertyId);
                    insertImg.setString(2, secImg);
                    insertImg.executeUpdate();
                }
            }
        }
    }

    private long propertyId(Connection connection, String title) throws SQLException {
        try (PreparedStatement statement = connection.prepareStatement("SELECT id FROM properties WHERE title = ?")) {
            statement.setString(1, title);
            try (ResultSet rs = statement.executeQuery()) {
                if (!rs.next()) {
                    throw new SQLException("Missing seeded property: " + title);
                }
                return rs.getLong("id");
            }
        }
    }

    private void insertBooking(Connection connection, long userId, long propertyId, String checkIn, String checkOut,
                               String paymentMethod, String paymentStatus, String status) throws SQLException {
        try (PreparedStatement statement = connection.prepareStatement(
                """
                        INSERT INTO bookings(user_id, property_id, check_in, check_out, nights, total_amount, payment_method, payment_status, status)
                        SELECT ?, ?, ?, ?, DATEDIFF('DAY', CAST(? AS DATE), CAST(? AS DATE)), p.nightly_rate * DATEDIFF('DAY', CAST(? AS DATE), CAST(? AS DATE)), ?, ?, ?
                        FROM properties p WHERE p.id = ?
                        """)) {
            statement.setLong(1, userId);
            statement.setLong(2, propertyId);
            statement.setDate(3, java.sql.Date.valueOf(checkIn));
            statement.setDate(4, java.sql.Date.valueOf(checkOut));
            statement.setString(5, checkIn);
            statement.setString(6, checkOut);
            statement.setString(7, checkIn);
            statement.setString(8, checkOut);
            statement.setString(9, paymentMethod);
            statement.setString(10, paymentStatus);
            statement.setString(11, status);
            statement.setLong(12, propertyId);
            statement.executeUpdate();
        }
    }
