    @Override
    public void contextInitialized(ServletContextEvent sce) {
        DatabaseUtil.init();
        try (Connection connection = DatabaseUtil.getConnection()) {
            createTables(connection);
            seedData(connection);
        } catch (SQLException e) {
            throw new IllegalStateException("Database startup failed", e);
        }
    }

    @Override
    public void contextDestroyed(ServletContextEvent sce) {
        DatabaseUtil.shutdown();
    }

    private void createTables(Connection connection) throws SQLException {
        try (Statement statement = connection.createStatement()) {
            statement.execute("""
                    CREATE TABLE IF NOT EXISTS users (
                        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        name VARCHAR(120) NOT NULL,
                        email VARCHAR(180) NOT NULL UNIQUE,
                        password_hash VARCHAR(500),
                        role VARCHAR(20) NOT NULL,
                        auth_provider VARCHAR(30) NOT NULL,
                        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
                    )
                    """);
            statement.execute("""
                    CREATE TABLE IF NOT EXISTS properties (
                        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        title VARCHAR(180) NOT NULL,
                        city VARCHAR(80) NOT NULL,
                        address VARCHAR(240) NOT NULL,
                        nightly_rate DECIMAL(12, 2) NOT NULL,
                        image_url VARCHAR(500),
                        source_url VARCHAR(500),
                        latitude DECIMAL(9, 6) DEFAULT 6.5244,
                        longitude DECIMAL(9, 6) DEFAULT 3.3792,
                        wifi BOOLEAN DEFAULT FALSE,
                        self_check_in BOOLEAN DEFAULT FALSE,
                        near_airport BOOLEAN DEFAULT FALSE,
                        reply_time_minutes INT DEFAULT 30,
                        rating DECIMAL(3, 2) DEFAULT 4.5
                    )
                    """);
            statement.execute("""
                    CREATE TABLE IF NOT EXISTS bookings (
                        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        user_id BIGINT NOT NULL,
                        property_id BIGINT NOT NULL,
                        check_in DATE NOT NULL,
                        check_out DATE NOT NULL,
                        nights INT DEFAULT 1,
                        total_amount DECIMAL(12, 2) DEFAULT 0,
                        payment_method VARCHAR(40) DEFAULT 'PAY_ON_ARRIVAL',
                        payment_status VARCHAR(30) DEFAULT 'PENDING',
                        status VARCHAR(30) NOT NULL,
                        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                        CONSTRAINT fk_booking_user FOREIGN KEY (user_id) REFERENCES users(id),
                        CONSTRAINT fk_booking_property FOREIGN KEY (property_id) REFERENCES properties(id)
                    )
                    """);
            statement.execute("""
                    CREATE TABLE IF NOT EXISTS property_images (
                        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        property_id BIGINT NOT NULL,
                        image_url VARCHAR(500) NOT NULL,
                        CONSTRAINT fk_image_property FOREIGN KEY (property_id) REFERENCES properties(id)
                    )
                    """);
            statement.execute("""
                    CREATE TABLE IF NOT EXISTS wishlist (
                        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        user_id BIGINT NOT NULL,
                        property_id BIGINT NOT NULL,
                        CONSTRAINT fk_wishlist_user FOREIGN KEY (user_id) REFERENCES users(id),
                        CONSTRAINT fk_wishlist_property FOREIGN KEY (property_id) REFERENCES properties(id),
                        CONSTRAINT uq_wishlist UNIQUE (user_id, property_id)
                    )
                    """);
            statement.execute("""
                    CREATE TABLE IF NOT EXISTS property_views (
                        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        property_id BIGINT NOT NULL,
                        viewed_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                        CONSTRAINT fk_view_property FOREIGN KEY (property_id) REFERENCES properties(id)
                    )
                    """);
            statement.execute("""
                    CREATE TABLE IF NOT EXISTS search_history (
                        id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        user_id BIGINT NOT NULL,
                        city VARCHAR(80) NOT NULL,
                        searched_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                        CONSTRAINT fk_history_user FOREIGN KEY (user_id) REFERENCES users(id)
                    )
                    """);
            addColumnIfMissing(statement, "properties", "source_url", "VARCHAR(500)");
            addColumnIfMissing(statement, "properties", "latitude", "DECIMAL(9, 6) DEFAULT 6.5244");
            addColumnIfMissing(statement, "properties", "longitude", "DECIMAL(9, 6) DEFAULT 3.3792");
            addColumnIfMissing(statement, "properties", "wifi", "BOOLEAN DEFAULT FALSE");
            addColumnIfMissing(statement, "properties", "self_check_in", "BOOLEAN DEFAULT FALSE");
            addColumnIfMissing(statement, "properties", "near_airport", "BOOLEAN DEFAULT FALSE");
            addColumnIfMissing(statement, "properties", "reply_time_minutes", "INT DEFAULT 30");
            addColumnIfMissing(statement, "properties", "rating", "DECIMAL(3, 2) DEFAULT 4.5");
            addColumnIfMissing(statement, "bookings", "nights", "INT DEFAULT 1");
            addColumnIfMissing(statement, "bookings", "total_amount", "DECIMAL(12, 2) DEFAULT 0");
            addColumnIfMissing(statement, "bookings", "payment_method", "VARCHAR(40) DEFAULT 'PAY_ON_ARRIVAL'");
            addColumnIfMissing(statement, "bookings", "payment_status", "VARCHAR(30) DEFAULT 'PENDING'");
            addColumnIfMissing(statement, "bookings", "created_at", "TIMESTAMP DEFAULT CURRENT_TIMESTAMP");
            statement.execute("""
                    UPDATE bookings b
                    SET nights = DATEDIFF('DAY', b.check_in, b.check_out)
                    WHERE nights IS NULL OR nights <= 0
                    """);
            statement.execute("""
                    UPDATE bookings b
                    SET total_amount = (
                        SELECT p.nightly_rate * b.nights FROM properties p WHERE p.id = b.property_id
                    )
                    WHERE total_amount IS NULL OR total_amount = 0
                    """);
        }
    }

